package CreationalPatterns.Builder;

public class Body {

	private int arms = 2;
	private int ears = 2;
	private int eyes = 2;
	public int getArms() {
		return arms;
	}
	public void setArms(int arms) {
		this.arms = arms;
	}
	public int getEars() {
		return ears;
	}
	public void setEars(int ears) {
		this.ears = ears;
	}
	public int getEyes() {
		return eyes;
	}
	public void setEyes(int eyes) {
		this.eyes = eyes;
	}
	
	
	
}
